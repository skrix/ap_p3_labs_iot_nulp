#include <stdio.h>
#include <stdlib.h>

#define SIZE 7

int main()
{
  int arr[SIZE];
  int *px = arr; // покажчик на масив
  int i;
  int min_odd;
  int found = 0; // прапорець для перевірки наявності непарних елементів

  // Перший елемент масиву дорівнює 201
  *px = 201;

  // Введення решти елементів масиву з клавіатури
  printf("Перший елемент arr[0] = %d (заданий)\n", *px);
  printf("\nВведіть ще %d елементів масиву:\n", SIZE - 1);

  for (i = 1; i < SIZE; i++)
  {
    printf("arr[%d] = ", i);
    scanf("%d", px + i); // доступ через покажчик
  }

  // Виведення всіх елементів масиву
  printf("\nЗначення елементів масиву:\n");
  for (i = 0; i < SIZE; i++)
  {
    printf("arr[%d] = %d\n", i, *(px + i));
  }

  // Пошук мінімального непарного елемента
  for (i = 0; i < SIZE; i++)
  {
    if (*(px + i) % 2 != 0)
    { // перевірка на непарність
      if (!found)
      {
        min_odd = *(px + i); // перший непарний елемент
        found = 1;
      }
      else if (*(px + i) < min_odd)
      {
        min_odd = *(px + i); // оновлення мінімуму
      }
    }
  }

  // Виведення результату
  if (found)
  {
    printf("\nМінімальний непарний елемент: %d\n", min_odd);
  }
  else
  {
    printf("\nУ масиві немає непарних елементів.\n");
  }

  return 0;
}

#include <stdio.h>
#include <stdlib.h>
#include <time.h>

#define N 10

int main()
{
  int dig[N];
  int i, j, mm, max, tmp;

  // Запуск генератора випадкових чисел
  srand((unsigned)time(NULL));

  // Заповнення масиву випадковими числами від -50 до 50
  printf("Заповнення масиву випадковими числами від -50 до 50\n\n");
  for (i = 0; i < N; i++)
  {
    dig[i] = rand() % 101 - 50; // діапазон від -50 до +50
  }

  // Виведення масиву до сортування
  printf("Масив ДО сортування:\n");
  for (i = 0; i < N; i++)
  {
    printf("%4d ", dig[i]);
  }
  printf("\n\n");

  // Сортування методом прямого упорядкування (метод вибору) за УБУВАННЯМ
  for (i = 0; i < N - 1; i++)
  {
    max = dig[i]; // припускаємо, що перший елемент максимальний
    mm = i;       // запам'ятовуємо його індекс

    // Шукаємо максимальний елемент у невідсортованій частині
    for (j = i + 1; j < N; j++)
    {
      if (dig[j] > max)
      {
        max = dig[j];
        mm = j;
      }
    }

    // Обмін місцями максимального елемента з поточним
    if (mm != i)
    {
      tmp = dig[i];
      dig[i] = dig[mm];
      dig[mm] = tmp;
    }
  }

  // Виведення масиву після сортування
  printf("Масив ПІСЛЯ сортування за убуванням:\n");
  for (i = 0; i < N; i++)
  {
    printf("%4d ", dig[i]);
  }
  printf("\n");

  return 0;
}
